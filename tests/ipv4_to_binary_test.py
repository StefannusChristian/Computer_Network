from ip_converter.ip4_to_binary import convert_ip4_to_binary


def test_ip_address_to_binary():
    ip_addresses = ['108.78.154.65', '149.77.68.12', '110.218.245.87', '31.43.145.147', '215.189.199.121',
                    '246.179.48.184', '15.217.34.130', '144.222.111.162', '151.79.14.47', '26.152.20.52']

    expected = [
        '01101100.01001110.10011010.01000001',
        '10010101.01001101.01000100.00001100',
        '01101110.11011010.11110101.01010111',
        '00011111.00101011.10010001.10010011',
        '11010111.10111101.11000111.01111001',
        '11110110.10110011.00110000.10111000',
        '00001111.11011001.00100010.10000010',
        '10010000.11011110.01101111.10100010',
        '10010111.01001111.00001110.00101111',
        '00011010.10011000.00010100.00110100'
    ]
    j = 0
    for ip in ip_addresses:
        result = convert_ip4_to_binary(ip)
        assert expected[j] == result
        j += 1
